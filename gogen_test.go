// parseGo fails the test unless src is syntactically valid Go.
func parseGo(t *testing.T, src []byte) {
	t.Helper()
	if _, err := parser.ParseFile(token.NewFileSet(), "gen.go", src, parser.AllErrors); err != nil {
		t.Fatalf("generated source is not valid Go: %v\n---\n%s", err, src)
	}
}

// flat collapses all runs of whitespace to single spaces so substring
// assertions don't depend on gofmt's column alignment.
func flat(s string) string {
	return strings.Join(strings.Fields(s), " ")
}

func mustContain(t *testing.T, src []byte, wants ...string) {
	t.Helper()
	got := flat(string(src))
	for _, w := range wants {
		if !strings.Contains(got, flat(w)) {
			t.Errorf("generated source missing %q\n---\n%s", w, src)
		}
	}
}

func TestGenerateGo_basicStruct(t *testing.T) {
	schema := []byte(`{
		"title": "Person",
		"type": "object",
		"required": ["name"],
		"properties": {
			"name": {"type": "string", "description": "full name"},
			"age": {"type": "integer"},
			"tags": {"type": "array", "items": {"type": "string"}}
		}
	}`)
	src, err := jsonschema.GenerateGo(schema, jsonschema.WithGoPackage("people"))
	if err != nil {
		t.Fatalf("GenerateGo: %v", err)
	}
	parseGo(t, src)
	mustContain(t, src,
		"package people",
		"// Code generated by jsonschema; DO NOT EDIT.",
		"type Person struct",
		"// full name",
		"Name string `json:\"name\"`",
		"Age int `json:\"age,omitempty\"`",
		"Tags []string `json:\"tags,omitempty\"`",
	)
}

