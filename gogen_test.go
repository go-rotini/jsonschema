// structFieldType parses src and returns the Go type expression of
// structName.fieldName, as rendered by go/types.ExprString.
func structFieldType(t *testing.T, src []byte, structName, fieldName string) string {
	t.Helper()
	f, err := parser.ParseFile(token.NewFileSet(), "gen.go", src, 0)
	if err != nil {
		t.Fatalf("parse generated source: %v\n%s", err, src)
	}
	got := ""
	ast.Inspect(f, func(n ast.Node) bool {
		ts, ok := n.(*ast.TypeSpec)
		if !ok || ts.Name.Name != structName {
			return true
		}
		st, ok := ts.Type.(*ast.StructType)
		if !ok {
			return true
		}
		for _, fld := range st.Fields.List {
			if len(fld.Names) == 1 && fld.Names[0].Name == fieldName {
				got = types.ExprString(fld.Type)
			}
		}
		return false
	})
	if got == "" {
		t.Fatalf("field %s.%s not found in:\n%s", structName, fieldName, src)
	}
	return got
}

// optionalFieldType generates types for a single optional "field" property
// described by propSchema and returns that field's Go type. A "Ref" object
// definition is always available for $ref cases.
func optionalFieldType(t *testing.T, propSchema string) string {
	t.Helper()
	schema := `{
		"$schema": "http://json-schema.org/draft-07/schema#",
		"type": "object",
		"definitions": {"Ref": {"type": "object", "properties": {"a": {"type": "string"}}}},
		"properties": {"field": ` + propSchema + `}
	}`
	src, err := jsonschema.GenerateGo([]byte(schema), jsonschema.WithGoRootType("Root"))
	if err != nil {
		t.Fatalf("GenerateGo(%s): %v", propSchema, err)
	}
	return structFieldType(t, src, "Root", "Field")
}

// TestGenerateGo_typeMatrix exercises the type mapping across the full range
// of JSON Schema constructs the generator recognizes, plus the constructs it
// deliberately degrades to any (unions, oneOf/anyOf/not, conditionals,
// boolean schemas, empty schemas). Fields here are optional, so scalars are
// value types and object references are pointers.
func TestGenerateGo_typeMatrix(t *testing.T) {
	cases := []struct {
		name string
		prop string
		want string
	}{
		// Scalars.
		{"string", `{"type":"string"}`, "string"},
		{"integer", `{"type":"integer"}`, "int"},
		{"number", `{"type":"number"}`, "float64"},
		{"boolean", `{"type":"boolean"}`, "bool"},

		// Arrays.
		{"array_of_string", `{"type":"array","items":{"type":"string"}}`, "[]string"},
		{"array_of_integer", `{"type":"array","items":{"type":"integer"}}`, "[]int"},
		{"array_of_ref", `{"type":"array","items":{"$ref":"#/definitions/Ref"}}`, "[]Ref"},
		{"array_no_items", `{"type":"array"}`, "[]any"},
		{"array_tuple_items", `{"type":"array","items":[{"type":"string"},{"type":"integer"}]}`, "[]any"},

		// Objects / maps.
		{"map_string", `{"type":"object","additionalProperties":{"type":"string"}}`, "map[string]string"},
		{"map_ref", `{"type":"object","additionalProperties":{"$ref":"#/definitions/Ref"}}`, "map[string]Ref"},
		{"map_addl_true", `{"type":"object","additionalProperties":true}`, "map[string]any"},
		{"free_object", `{"type":"object"}`, "map[string]any"},
		{"pattern_properties", `{"type":"object","patternProperties":{"^x":{"type":"integer"}}}`, "map[string]int"},

		// References.
		{"ref", `{"$ref":"#/definitions/Ref"}`, "*Ref"},

		// enum / const inference.
		{"enum_string", `{"enum":["a","b"]}`, "string"},
		{"enum_number", `{"enum":[1,2]}`, "float64"},
		{"const_string", `{"const":"x"}`, "string"},
		{"const_bool", `{"const":true}`, "bool"},

		// Type unions.
		{"nullable_string", `{"type":["string","null"]}`, "string"},
		{"union_multi", `{"type":["string","integer"]}`, "any"},

		// Constructs with no faithful Go mapping: degrade to any.
		{"oneOf", `{"oneOf":[{"type":"string"},{"type":"integer"}]}`, "any"},
		{"anyOf", `{"anyOf":[{"type":"string"},{"type":"boolean"}]}`, "any"},
		{"not", `{"not":{"type":"string"}}`, "any"},
		{"empty_schema", `{}`, "any"},
		{"boolean_schema_true", `true`, "any"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			if got := optionalFieldType(t, tc.prop); got != tc.want {
				t.Errorf("type for %s = %q, want %q", tc.prop, got, tc.want)
			}
		})
	}
}

// TestGenerateGo_requiredFields verifies that required scalars are value
// types without omitempty and that a required object reference is a value
// (not a pointer).
func TestGenerateGo_requiredFields(t *testing.T) {
	schema := []byte(`{
		"type": "object",
		"required": ["name", "ref"],
		"definitions": {"Ref": {"type": "object", "properties": {"a": {"type": "string"}}}},
		"properties": {
			"name": {"type": "string"},
			"opt": {"type": "string"},
			"ref": {"$ref": "#/definitions/Ref"}
		}
	}`)
	src, err := jsonschema.GenerateGo(schema, jsonschema.WithGoRootType("Root"))
	if err != nil {
		t.Fatalf("GenerateGo: %v", err)
	}
	parseGo(t, src)
	// A required object reference is a value type, not a pointer.
	if got := structFieldType(t, src, "Root", "Ref"); got != "Ref" {
		t.Errorf("required ref type = %q, want %q", got, "Ref")
	}
	mustContain(t, src,
		"Name string `json:\"name\"`",         // required: no omitempty
		"Opt string `json:\"opt,omitempty\"`", // optional: omitempty
	)
}

